<?php
$num = -15.75;
echo "Absolute: " . abs($num) . "\n";
echo "Ceiling: " . ceil($num) . "\n";
echo "Floor: " . floor($num) . "\n";
echo "Square Root: " . sqrt(16) . "\n";
?>


output:
