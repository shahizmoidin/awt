//3. Important Methods of Date Object

let date = new Date();
console.log(date.getFullYear()); // Method: getFullYear()
console.log(date.getMonth() + 1); // Method: getMonth() (0-11, so +1 for correct month)
console.log(date.getDate()); // Method: getDate()
console.log(date.toDateString()); // Method: toDateString()

